const parseDate = (s: string) => {
  if (!s) return new Date("1970-01-01");
  if (s.includes("-")) return new Date(s);
  if (s.includes("/")) {
    const parts = s.split("/").map((p) => parseInt(p, 10));
    if (parts.length === 3) {
      const [d, m, y] = parts;
      return new Date(y, m - 1, d);
    }
  }
  return new Date(s);
};

