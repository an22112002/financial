  today.setHours(0, 0, 0, 0);

  contractsCalendar.filter((c) => c.type === "receive").forEach((c) => {
    const expected = c.money;
    const payments = moneyFlows.filter((m) => {
      if (m.type !== "receive") return false;
      if (c.id_payment && m.id_payment && m.id_payment === c.id_payment) return true;
      if (c.id_contract && m.id_contract && m.id_contract === c.id_contract) return true;
      if (c.id_contract && m.description && m.description.includes(c.id_contract)) return true;
      if (m.date === c.date && m.amount === c.money) return true;
      return false;
    });

    const txs = payments.map((m) => ({ id_transaction: m.id_payment, account: m.account, partner: m.exchange, amount: m.amount, paytime: `${m.date} 00:00:00`, status: "marked" }));
    const paid = txs.reduce((s, t) => s + t.amount, 0);
    const remaining = expected - paid;

    if (paid > 0) {
      const status = paid >= expected ? "paided" : "not_enough";
      derivedCompletedReceive.push({ partner: c.id_contract ?? c.title, contractTitle: c.title, amount: expected, paytime: c.date, lastTime: c.date, late: 0, latePee: 0, status, payments: txs });
    }

    if (remaining > 0) {
      let status: string = "pending";
      if (paid > 0 && paid < expected) status = "not_enough";
      else {
        const due = parseDate(c.date);
        due.setHours(0, 0, 0, 0);
        if (due < today) status = "overdue";
        else status = "pending";
      }
      derivedPendingReceives.push({ partner: c.id_contract ?? c.title, amount: remaining, paytime: c.date, lastTime: c.date, status });
    }
  });

